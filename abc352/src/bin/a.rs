#[allow(non_snake_case)]
#[allow(unused_variables)]
#[fastout]
fn main() {
    input! {
        n: usize, x: usize,
        y: usize, z: usize,
    }
    let stop = if x < y {
        x <= z && z <= y
    } else {
        y <= z && z <= x
    };

    println!("{}", if stop { "Yes" } else { "No" });
}
